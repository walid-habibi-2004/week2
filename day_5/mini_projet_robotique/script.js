let robots2 = robots
const main = document.querySelector("main")
const display = (robots) => {

    for (const robot of robots) {
        let div = document.createElement("div")
        div.setAttribute("id", "cards")
        main.appendChild(div)
        let h2 = document.createElement("h2")
        h2.textContent = robot.name
        let p = document.createElement("p")
        p.textContent = robot.email
        let image = document.createElement("img")
        image.setAttribute("src", robot.image)

        div.appendChild(image)
        div.appendChild(h2)
        div.appendChild(p)
    }
}

display(robots)

const input = document.getElementById("search")
input.addEventListener("input", (e) => {

    let search = robots.filter(robot => robot.name.toLowerCase().startsWith(e.target.value.toLowerCase()))
    if (search) {
        main.innerHTML = ""
        display(search)
    } else {
        let p = document.createElement("p")
        p.textContent = "Item not found"
    }

})
